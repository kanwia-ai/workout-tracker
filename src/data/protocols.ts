// ─── Specialized Protocols ──────────────────────────────────────────────────
// Structured rehab/strengthening programs for knee recovery + glute development

import type { KneeSafety, Equipment, MuscleGroup } from '../types'

// ─── Protocol Types ─────────────────────────────────────────────────────────

export interface ProtocolExercise {
  id: string
  name: string
  sets: number
  reps: string
  rest_seconds: number
  track_weight: boolean
  equipment: Equipment[]
  primary_muscles: MuscleGroup[]
  knee_safety: KneeSafety
  cues: string[]
  note?: string
  work_seconds?: number // for timed/isometric exercises
}

export interface ProtocolWeek {
  week: number
  label: string
  focus: string
  exercises: ProtocolExercise[]
}

export interface Protocol {
  id: string
  name: string
  emoji: string
  description: string
  total_weeks: number
  weeks: ProtocolWeek[]
  pain_check_enabled: boolean
}

export type PainLevel = 'none' | 'mild' | 'moderate' | 'severe'

export interface ProtocolProgress {
  protocol_id: string
  current_week: number
  started_at: string
  status: 'active' | 'paused' | 'completed'
  sessions_completed: number
  pain_logs: PainLog[]
  completed_exercises: Record<string, boolean> // key: "week-exerciseId"
}

export interface PainLog {
  date: string
  week: number
  pain_level: PainLevel
  swelling: boolean
  notes?: string
  auto_regressed: boolean
}

