// ─── Lookup helpers ─────────────────────────────────────────────────────────

/** Get an exercise by ID */
export function getExerciseById(id: string): Exercise | undefined {
  return EXERCISE_LIBRARY.find(e => e.id === id)
}

/** Get exercise name by ID (for backwards compat with EXERCISE_NAMES) */
export function getExerciseName(id: string): string {
  return EXERCISE_LIBRARY.find(e => e.id === id)?.name ?? id
}

/** Build a name lookup map (to replace EXERCISE_NAMES in WorkoutView) */
export function buildExerciseNameMap(): Record<string, string> {
  const map: Record<string, string> = {}
  for (const ex of EXERCISE_LIBRARY) {
    map[ex.id] = ex.name
  }
  return map
}

/** Get unique muscle groups from the library */
export function getUniqueMuscleGroups(): string[] {
  const set = new Set<string>()
  for (const ex of EXERCISE_LIBRARY) {
    for (const m of ex.primary_muscles) set.add(m)
  }
  return Array.from(set).sort()
}

/** Get unique equipment from the library */
export function getUniqueEquipment(): string[] {
  const set = new Set<string>()
  for (const ex of EXERCISE_LIBRARY) {
    for (const e of ex.equipment) set.add(e)
  }
  return Array.from(set).sort()
}
